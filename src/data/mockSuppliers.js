export function findSupplier(id) {
  return SUPPLIERS.find((s) => s.id === id) ?? null;
}

export function findSupplierByName(name) {
  return SUPPLIERS.find((s) => s.name === name) ?? null;
}

export function summarizeSuppliers() {
  const total = SUPPLIERS.length;
  const byTier = SUPPLIERS.reduce((acc, s) => {
    acc[s.tier] = (acc[s.tier] ?? 0) + 1;
    return acc;
  }, {});
  const avgOnTime =
    SUPPLIERS.reduce((s, x) => s + x.onTimeRate, 0) / total;
  const review = byTier.review ?? 0;
  return { total, byTier, avgOnTime, review };
}
