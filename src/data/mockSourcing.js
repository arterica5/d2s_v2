export function findRfx(id) {
  return RFX_LIST.find((r) => r.id === id) ?? null;
}

export function summarizeRfx() {
  const byStatus = RFX_LIST.reduce((acc, r) => {
    acc[r.status] = (acc[r.status] ?? 0) + 1;
    return acc;
  }, {});
  const active = RFX_LIST.filter((r) =>
    ["sent", "responded", "comparing"].includes(r.status),
  ).length;
  const awarded = byStatus.awarded ?? 0;
  const pendingResponses = RFX_LIST.filter(
    (r) => r.status === "sent",
  ).reduce((s, r) => s + (r.invitees - r.responses), 0);
  return {
    total: RFX_LIST.length,
    active,
    awarded,
    pendingResponses,
    byStatus,
  };
}
